#include "ft_printf.h"

int		ft_isalpha(int str)
{
	int i;

	i = 0;
	if (str >= 'A' && str <= 'Z')
	{
		return (1);
	}
	else if (str >= 'a' && str <= 'z')
	{
		return (2);
	}
	else
	{
		return (0);
	}
}
